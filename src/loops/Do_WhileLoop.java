package loops;

public class Do_WhileLoop {

	public static void main(String[] args) {
		//want to print 1-10
		int i=1;
		
		do
		{
			System.out.println(i);
			i=i+1;
		}
		
		while(i<=10);
		
		System.out.println();
		
		int j=1;
		do 
		{
			System.out.println(j);
			j=j+1;
		}
          
		while (j<=0);
		
		System.out.println();
			
		//want to print 1-10 with gap of 3
        int k=1;
		
		do
		{
			System.out.println(k);
			k=k+3;
		}
		
		while(k<=10);
		
		System.out.println();
		
		//want to print 1-50 
        int l=1;
		
		do
		{
			System.out.println(l);
			l=l+1;
		}
		
		while(l<=50);
		
		System.out.println();
		
		//want to print 30-1 
        int m=30;
		
		do
		{
			System.out.println(m);
			m=m-1;
		}
		
		while(m>=1);
		
		System.out.println();
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
