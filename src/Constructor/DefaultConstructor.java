package Constructor;

public class DefaultConstructor {
	
	public DefaultConstructor()//default construction 
	{
		
		
	}
	
	

	public static void main(String[] args) {
		DefaultConstructor C1=new DefaultConstructor();//object creat
		C1.myMyethod();
		  

	}
	public void myMyethod()
	{
		System.out.println(" hi this is default constructor example");
	}
}
