package variableStudy;

public class EmployeedetailPractise {

	public static void main(String[] args) {
		// 1.variable declaration
		String employeename;
		byte employeeid;
		int employeesalary;
		char grade;
		float employeeperformance;
		
		// 2.variable initialization
		employeename="Mukta kale";
		employeeid=120;
		employeesalary=40000;
		grade='A';
		employeeperformance=100f;
		
		// 3.usage
		System.out.println("My name is "+employeename);
		System.out.println("My Employee Id is "+employeeid);
		System.out.println("My salary per month is "+employeesalary);
		System.out.println("I got promotion in company on my work performance is grade "+grade);
		System.out.println("My performance is also "+employeeperformance+" %");
		
	}

}
